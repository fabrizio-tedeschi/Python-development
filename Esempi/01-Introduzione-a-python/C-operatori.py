#Definizione di due variabili x e y
x = 10
y = 7

#Stampa dei valori su output
print("Situazione iniziale:")
print("La variabile x vale", x)
print("La variabile y vale", y)
print()

#Stampa di operazioni
print("La somma x+y vale:", x + y)
print("La differenza x-y vale:", x - y)
print("La moltiplicazione x*y vale:", x * y)
print("La divisione x/y vale:", x/y)
print("Il resto di x/y ottenuto con % vale:", x % y)
print()

#Stampa dei confonti
print("Controllo se x == y:", x == y)
print("Controllo se x != y:", x != y)

#Definizione di variabili che contengono stringhe
s1 = "Prima stringa lunga"
s2 = "altra stringa"
s3 = "altra stringa"

print("Le tre stringhe sono:")
print("-->", s1)
print("-->", s2)
print("-->", s3)
print()

print("L'operatore == confronta ciascun carattere della stringa:")
print("Risultato del confronto s1 == s2:", s1 == s2)
print("Risultato del confronto s3 == s2:", s3 == s2)
print()
print("Gli operatori > e < confrontano l'ordine alfabetico:")
print("Risultato del confronto s1 > s2:", s1 > s2)
print("Risultato del confronto s1 < s2:", s1 < s2)
print()

#Il codice seguente produce errore
print("PROVO UNA SOTTRAZIONE FRA STRINGE:")
print("Risultato del confronto s1 - s2:", s1 - s2)