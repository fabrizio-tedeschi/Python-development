#Inizializzazione della tupla
orto = ("Mele", "Pere", "Banane", "Fragole", "Melone", "Ciliege")

#Inizializzazione di un contatore per l'indice di posizione
index = 0

#Iterazione sulla lista
for frutto in orto:

    #Stampa dell'elemento e della sua posizione
    print("In posizione", index, "si trova l'elemento:", frutto)

    #Aggiornamento dell'indice di posizione
    index += 1